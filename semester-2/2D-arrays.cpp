#include <iostream>
using namespace std;
int main()
{
    int sum = 0;
    int sum2 = 0;
    int j = 0;
    char arr[3][3] = {9, 9, 3, 3, 9, 6, 7, 9, 9};
    for (int i = 0; i < 3; i++)
    {
        sum = arr[i][j] + sum;
        j++;
    }
    cout << " the sum of right diagnol is " << sum << endl;
    int k = 3;
    for (int i = 0; i < 3; i++)
    {
        sum2 = arr[i][k] + sum2;
        k--;
    }
    cout << "the sum of left diagnol is " << sum2;

    return 0;
}